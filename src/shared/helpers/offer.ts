import { Convenience, Offer, OfferType, UserType } from '../types/index.js';

export function createOffer(offerData: string): Offer {
  const [
    title,
    description,
    postDate,
    city,
    cityLatitude,
    cityLongitude,
    previewPath,
    images,
    isPremium,
    isFavorite,
    rating,
    type,
    roomsCount,
    guestsCount,
    price,
    conveniences,
    authorName,
    authorEmail,
    authorAvatarPath,
    authorPassword,
    authorType,
    commentsCount,
    latitude,
    longitude,
  ] = offerData.replace('\n', '').split('\t');

  const author = {
    name: authorName,
    email: authorEmail,
    avatar: authorAvatarPath,
    password: authorPassword,
    type: UserType[authorType as keyof typeof UserType]
  };

  const offerCity = {
    name: city,
    latitude: Number.parseFloat(cityLatitude),
    longitude: Number.parseFloat(cityLongitude)
  };

  return {
    title,
    description,
    postDate: new Date(postDate),
    city: offerCity,
    previewPath,
    images: images.split(','),
    isPremium: isPremium === 'true',
    isFavorite: isFavorite === 'true',
    rating: Number.parseFloat(rating),
    type: OfferType[type as keyof typeof OfferType],
    roomsCount: Number.parseInt(roomsCount, 10),
    guestsCount: Number.parseInt(guestsCount, 10),
    price: Number.parseInt(price, 10),
    conveniences: conveniences.split(',').map((convenience) => Convenience[convenience as keyof typeof Convenience]),
    author,
    commentsCount: Number.parseInt(commentsCount, 10),
    latitude: Number.parseFloat(latitude),
    longitude: Number.parseFloat(longitude)
  };
}
